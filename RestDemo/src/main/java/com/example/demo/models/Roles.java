package com.example.demo.models;

public class Roles
{
    int id;
    int idofuser;
    String role;

    public Roles() {}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdofuser() {
        return idofuser;
    }

    public void setIdofuser(int idofuser) {
        this.idofuser = idofuser;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
